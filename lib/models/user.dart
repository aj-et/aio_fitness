class User {
  final String username;
  final String email;
  final int age;
  final double height;
  final double weight;
  final String fitnessGoal;
  final String activityLevel;

  User({
    required this.username,
    required this.email,
    required this.age,
    required this.height,
    required this.weight,
    required this.fitnessGoal,
    required this.activityLevel,
  });
}